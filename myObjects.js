Graphic = function Graphic()
{
this.width = 0;
this.height = 0;
};

Src = function Src()
{
    this.x = 0;
    this.y = 0;
    this.curX = 0;
    this.curY = 0;
    this.scale = 1;
    this.width = 0;
    this.height = 0;
    this.mouseX = 0;
    this.mouseY = 0;
    this.ismouse = false;

}

Obj = function Obj(x,y,size,color)
{
    this.x = x;
    this.y = y;
    this.size = size;
    this.color = color;
}
